# запрашиваем строку у пользователя
user_input = input("Введите строку: ")
# разбиваем строку на слова, используя пробелы в качестве разделителей
words = user_input.split()
# извлекаем первые буквы каждого слова и формируем новую строку
initials = ' '.join(word[0] for word in words)
# выводим новую строку, состоящую из первых букв
print(f"Новая строка из первых букв: {initials}")
